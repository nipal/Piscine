#include "func.h"

long	ft_add(long a, long b)
{
	return (a + b);
}

long	ft_sub(long a, long b)
{
	return (a - b);
}

long	ft_mul(long a, long b)
{
	return (a * b);
}

long	ft_div(long a, long b)
{
	return (a / b);
}

long	ft_mod(long a, long b)
{
	return (a % b);
}
